const state = {
  lang: localStorage.getItem("profile-lang") || "zh",
  theme: localStorage.getItem("profile-theme") || "light"
};

const root = document.documentElement;
const langToggle = document.querySelector("#langToggle");
const themeToggle = document.querySelector("#themeToggle");

function setTheme(theme) {
  state.theme = theme;
  root.dataset.theme = theme;
  localStorage.setItem("profile-theme", theme);
  themeToggle.setAttribute("aria-label", theme === "dark" ? "Switch to light theme" : "Switch to dark theme");
}

function setLanguage(lang) {
  state.lang = lang;
  document.documentElement.lang = lang === "zh" ? "zh-Hant" : "en";
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = translations[lang][key] || node.textContent;
  });
  langToggle.textContent = lang === "zh" ? "EN" : "中";
  localStorage.setItem("profile-lang", lang);

  if (window.gsap) {
    gsap.fromTo("[data-i18n]", { y: 8, opacity: 0.68 }, {
      y: 0,
      opacity: 1,
      duration: 0.36,
      ease: "power2.out",
      stagger: 0.006
    });
  }
}

langToggle.addEventListener("click", () => setLanguage(state.lang === "zh" ? "en" : "zh"));
themeToggle.addEventListener("click", () => setTheme(state.theme === "dark" ? "light" : "dark"));

setTheme(state.theme);
setLanguage(state.lang);

function initGsap() {
  if (!window.gsap) {
    document.querySelectorAll("[data-reveal]").forEach((el) => {
      el.style.opacity = 1;
      el.style.transform = "none";
    });
    document.querySelectorAll(".bar i").forEach((bar) => {
      bar.style.width = bar.style.getPropertyValue("--value");
    });
    return;
  }

  gsap.registerPlugin(ScrollTrigger);

  gsap.to("[data-reveal]", {
    opacity: 1,
    y: 0,
    duration: 0.9,
    ease: "power3.out",
    stagger: 0.08,
    scrollTrigger: {
      trigger: "body",
      start: "top 86%"
    }
  });

  document.querySelectorAll("[data-reveal]").forEach((el) => {
    gsap.fromTo(el, { opacity: 0, y: 44 }, {
      opacity: 1,
      y: 0,
      duration: 0.85,
      ease: "power3.out",
      scrollTrigger: {
        trigger: el,
        start: "top 86%",
        toggleActions: "play none none reverse"
      }
    });
  });

  gsap.from(".hero-title span", {
    yPercent: 115,
    rotateX: -70,
    opacity: 0,
    duration: 1.1,
    ease: "expo.out",
    stagger: 0.12
  });

  gsap.to(".hero-visual img", {
    y: -18,
    rotate: 1.5,
    duration: 3.2,
    ease: "sine.inOut",
    repeat: -1,
    yoyo: true
  });

  gsap.to(".floating-panel", {
    y: -16,
    duration: 2.4,
    ease: "sine.inOut",
    repeat: -1,
    yoyo: true,
    stagger: 0.35
  });

  gsap.to(".bar i", {
    width: (_, el) => el.style.getPropertyValue("--value"),
    duration: 1.3,
    ease: "expo.out",
    stagger: 0.12,
    scrollTrigger: {
      trigger: ".skill-board",
      start: "top 78%"
    }
  });

  document.querySelectorAll(".counter").forEach((counter) => {
    const target = Number(counter.dataset.target);
    const decimals = String(target).includes(".") ? 2 : 0;
    gsap.to(counter, {
      textContent: target,
      duration: 1.4,
      ease: "power2.out",
      snap: { textContent: decimals ? 0.01 : 1 },
      onUpdate: () => {
        counter.textContent = Number(counter.textContent).toFixed(decimals);
      },
      scrollTrigger: {
        trigger: counter,
        start: "top 86%",
        once: true
      }
    });
  });
}

function initTiltCards() {
  const cards = document.querySelectorAll(".tilt-card");

  cards.forEach((card) => {
    card.addEventListener("pointermove", (event) => {
      const rect = card.getBoundingClientRect();
      const x = (event.clientX - rect.left) / rect.width - 0.5;
      const y = (event.clientY - rect.top) / rect.height - 0.5;
      card.style.transform = `rotateX(${y * -8}deg) rotateY(${x * 10}deg) translateY(-4px)`;
    });

    card.addEventListener("pointerleave", () => {
      card.style.transform = "";
    });
  });
}

function initMagneticButtons() {
  document.querySelectorAll(".magnetic").forEach((button) => {
    button.addEventListener("pointermove", (event) => {
      const rect = button.getBoundingClientRect();
      const x = event.clientX - rect.left - rect.width / 2;
      const y = event.clientY - rect.top - rect.height / 2;
      button.style.transform = `translate(${x * 0.18}px, ${y * 0.24}px)`;
    });

    button.addEventListener("pointerleave", () => {
      button.style.transform = "";
    });
  });
}

function initParticles() {
  const canvas = document.querySelector("#particleCanvas");
  const ctx = canvas.getContext("2d");
  const particles = [];
  const pointer = { x: 0, y: 0, active: false };
  let width = 0;
  let height = 0;
  let rafId = 0;

  function resize() {
    const dpr = Math.min(window.devicePixelRatio || 1, 2);
    width = window.innerWidth;
    height = window.innerHeight;
    canvas.width = Math.floor(width * dpr);
    canvas.height = Math.floor(height * dpr);
    canvas.style.width = `${width}px`;
    canvas.style.height = `${height}px`;
    ctx.setTransform(dpr, 0, 0, dpr, 0, 0);

    particles.length = 0;
    const count = Math.min(92, Math.max(42, Math.floor(width / 18)));
    for (let i = 0; i < count; i += 1) {
      particles.push({
        x: Math.random() * width,
        y: Math.random() * height,
        vx: (Math.random() - 0.5) * 0.55,
        vy: (Math.random() - 0.5) * 0.55,
        r: Math.random() * 2.2 + 0.8
      });
    }
  }

  function draw() {
    ctx.clearRect(0, 0, width, height);
    const isDark = state.theme === "dark";
    const dot = isDark ? "rgba(0, 224, 255, 0.82)" : "rgba(0, 107, 234, 0.62)";
    const line = isDark ? "rgba(125, 255, 223, 0.13)" : "rgba(0, 107, 234, 0.12)";

    particles.forEach((p, index) => {
      p.x += p.vx;
      p.y += p.vy;

      if (p.x < -20) p.x = width + 20;
      if (p.x > width + 20) p.x = -20;
      if (p.y < -20) p.y = height + 20;
      if (p.y > height + 20) p.y = -20;

      if (pointer.active) {
        const dx = pointer.x - p.x;
        const dy = pointer.y - p.y;
        const dist = Math.hypot(dx, dy);
        if (dist < 150) {
          p.x -= dx * 0.004;
          p.y -= dy * 0.004;
        }
      }

      ctx.beginPath();
      ctx.fillStyle = dot;
      ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2);
      ctx.fill();

      for (let j = index + 1; j < particles.length; j += 1) {
        const other = particles[j];
        const dx = p.x - other.x;
        const dy = p.y - other.y;
        const dist = Math.hypot(dx, dy);
        if (dist < 118) {
          ctx.strokeStyle = line;
          ctx.lineWidth = 1 - dist / 118;
          ctx.beginPath();
          ctx.moveTo(p.x, p.y);
          ctx.lineTo(other.x, other.y);
          ctx.stroke();
        }
      }
    });

    rafId = requestAnimationFrame(draw);
  }

  window.addEventListener("resize", resize);
  window.addEventListener("pointermove", (event) => {
    pointer.x = event.clientX;
    pointer.y = event.clientY;
    pointer.active = true;
  });
  window.addEventListener("pointerleave", () => {
    pointer.active = false;
  });

  resize();
  draw();

  document.addEventListener("visibilitychange", () => {
    if (document.hidden) {
      cancelAnimationFrame(rafId);
    } else {
      draw();
    }
  });
}

window.addEventListener("load", () => {
  initGsap();
  initTiltCards();
  initMagneticButtons();
  initParticles();
});
